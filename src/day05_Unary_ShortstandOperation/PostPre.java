package day05_Unary_ShortstandOperation;

public class PostPre {


    public static void main(String[] args) {



int a = 100;
        System.out.println(++a);
        System.out.println(a);

        int b =100;
        System.out.println(--b);
        System.out.println(b);

        //Post

        int A =100;
        System.out.println(A++);
        System.out.println(A);

        int B =100;
        System.out.println(B--);
        System.out.println(B);

//post & pre examples
        int z = 10;
        int X = ++z;
        System.out.println(X);
        System.out.println(z);









    }
}
