package day02_Variables;

public class EscapeSequences {


    public static void main(String[] args) {


        System.out.println("Hello");
        System.out.println("\tHello");
        System.out.println("\t\t\t\t\tHello");
        // it means a program space

        System.out.println("Cybertek School");
        System.out.println("Cybertek\nSchool");
        System.out.println("\n\nMy \nName \nIs \nHanna");
        //it means new line, starts with new line

        System.out.println("My Favorit TV show is \"Game of Thrones \"  ");

        System.out.println("My favorit book is\" Harry Potter\" ");

        System.out.println("\\");
        // It prints single quote in counsels
        System.out.println("/");
        System.out.println(" My favorite book is 'Java' ");




    }
}
